// for loop
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// for-of loop
let myArray = ["Luke", "Han", "Chewbacca", "Leia"];
for (const arrayItem of myArray) {
  console.log(arrayItem);
}

// while loop
let i = 1;
while (i < 5) {
  console.log(i);
  i++;
}

// do-while loop
let j = 1;
do {
  console.log(j);
  j++;
} while (j < 5);
